package Home;

public class MenuList {

	String item;
	String price;
	
	public void setMenuItem(String item) {
		this.item = item;
	}
	
	public void setItemPrice(String price) {
		this.price = price;
	}
	
	public String getMenuItem() {
		return item;
	}
	
	public String getItemPrice() {
		return price;
	}
}
